console.log("Comparision operator");
console.log(" 5 > 1 = ", 5 > 1); // greater than 
console.log(" 5 < 1 = ", 5 < 1);  // less than
console.log(" 5 == 5 =", 5 == 5);  // equal to
console.log(" 5 === '5' = ", 5 === '5'); // strict equal to
console.log("5 != 4 =", 5 != 4); // not equal to
console.log("5 >= 4 =", 5 >= 4); // greater than or equal to
console.log("5 <= 6 =", 5 <= 6); // less than or equal to
